#include "libft.h"

char	*ft_toupper(char *res)
{
	int	i;

	i = 0;
	while (res[i])
	{
		if (res[i] >= 97 && res[i] <= 122)
			res[i] -= 32;
		i++;
	}
	return (res);
}
